void ImprimirCampoJuego(int campoJuego[FILAS][COLUMNAS]) {
    for (int f = 0; f < FILAS; f++) {
        for (int c = 0; c < COLUMNAS; c++) {
            if (campoJuego[f][c] == 0) Console::ForegroundColor = ConsoleColor::DarkGreen;
            if (campoJuego[f][c] == 1) Console::ForegroundColor = ConsoleColor::White;
            if (campoJuego[f][c] == 2) Console::ForegroundColor = ConsoleColor::DarkGreen;
            if (campoJuego[f][c] == 3) Console::ForegroundColor = ConsoleColor::DarkBlue;
            if (campoJuego[f][c] == 4) Console::ForegroundColor = ConsoleColor::Red;
            if (campoJuego[f][c] == 5) Console::ForegroundColor = ConsoleColor::Gray;
            if (campoJuego[f][c] == 6) Console::ForegroundColor = ConsoleColor::Cyan;
            if (campoJuego[f][c] == 7) Console::ForegroundColor = ConsoleColor::Magenta;
            if (campoJuego[f][c] == 8) Console::ForegroundColor = ConsoleColor::Yellow;
            cout << (char)219;
        }
        Console::ResetColor(); // Resetea el color después de cada línea
        cout << endl;
    }
}
